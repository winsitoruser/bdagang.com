// Deep merge helper
function deepMerge(a: any, b: any): any {
  const result = { ...a };
  for (const key of Object.keys(b)) {
    if (b[key] && typeof b[key] === 'object' && !Array.isArray(b[key]) && a[key]) {
      result[key] = deepMerge(a[key], b[key]);
    } else {
      result[key] = b[key];
    }
  }
  return result;
}

export function mergeTranslations(
  base: Record<Language, Record<string, any>>,
  extra: Record<Language, Record<string, any>>
): Record<Language, Record<string, any>> {
  return {
    id: deepMerge(base.id || {}, extra.id || {}),
    en: deepMerge(base.en || {}, extra.en || {}),
    ja: deepMerge(base.ja || {}, extra.ja || {}),
    zh: deepMerge(base.zh || {}, extra.zh || {}),
  };
}

